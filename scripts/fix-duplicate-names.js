import Database from 'better-sqlite3';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(__dirname, '..');

const dbDir = '.wrangler/state/v3/d1/miniflare-D1DatabaseObject/';
const files = fs.readdirSync(dbDir).filter(f => f.endsWith('.sqlite') && f !== 'metadata.sqlite');
const dbPath = path.join(projectRoot, dbDir, files[0]);
const db = new Database(dbPath);

const apps = db.prepare('SELECT * FROM apps ORDER BY name').all();

const nameToApps = {};
for (const app of apps) {
  const key = app.name.toLowerCase().trim();
  if (!nameToApps[key]) nameToApps[key] = [];
  nameToApps[key].push(app);
}

const removals = [];

for (const [name, appList] of Object.entries(nameToApps)) {
  if (appList.length <= 1) continue;

  const scored = appList.map(app => {
    let score = 0;
    if (app.homepage && app.homepage.trim()) score += 3;
    if (app.description && app.description.length > 30) score += 2;
    if (app.linux_alternative && app.linux_alternative.trim()) score += 2;
    if (app.alternative_to && app.alternative_to.trim()) score += 1;
    if (app.popularity > 0) score += app.popularity > 50 ? 3 : 1;
    if (app.icon && app.icon.trim()) score += 1;
    return { app, score };
  });

  scored.sort((a, b) => b.score - a.score);

  const keepApp = scored[0].app;
  const losers = scored.slice(1);

  for (const loser of losers) {
    removals.push({ removeId: loser.app.id, keepId: keepApp.id, name: keepApp.name });
  }
}

console.log(`📊 ${removals.length} duplicate entries to remove:\n`);

const keepGroups = {};
for (const r of removals) {
  if (!keepGroups[r.keepId]) keepGroups[r.keepId] = { name: r.name, remove: [] };
  keepGroups[r.keepId].remove.push(r.removeId);
}

for (const [keepId, group] of Object.entries(keepGroups)) {
  console.log(`✅ KEEP: [${keepId}] ${group.name}`);
  console.log(`   ❌ REMOVE: ${group.remove.join(', ')}`);
}

let sql = '-- Remove duplicate apps (keep the one with more data)\n';
sql += '-- Generated by fix-duplicate-names.js\n\n';

for (const r of removals) {
  sql += `DELETE FROM reports WHERE app_id = '${r.removeId}';\n`;
  sql += `DELETE FROM apps WHERE id = '${r.removeId}';\n`;
}

fs.writeFileSync(path.join(projectRoot, 'db/fix-duplicates.sql'), sql);
console.log(`\n✅ SQL saved to db/fix-duplicates.sql (${removals.length} apps to remove)`);
